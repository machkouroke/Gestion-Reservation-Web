package com.lop.gestion.Beans;

public class Equipements {
    private int numEqt;
    private int numSalle;
    private char idBloc;
    private String type;

    public int getNumEqt() {
        return numEqt;
    }

    public void setNumEqt(int numEqt) {
        this.numEqt = numEqt;
    }

    public int getNumSalle() {
        return numSalle;
    }

    public void setNumSalle(int numSalle) {
        this.numSalle = numSalle;
    }

    public char getIdBloc() {
        return idBloc;
    }

    public void setIdBloc(char idBloc) {
        this.idBloc = idBloc;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
